// Converts Western digits to Arabic-Indic numerals when lang === "ar"
export function n(num, lang) {
  const s = String(num);
  if (lang !== "ar") return s;
  return s.replace(/[0-9]/g, d => "٠١٢٣٤٥٦٧٨٩"[+d]);
}

